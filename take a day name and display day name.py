# take a day then print the day name
day = int(input('Enter day: '))
if day == 1:
    print('Saturday')
elif day == 2:
    print('Sunday')
elif day == 3:
    print('Monday')
elif day == 4:
    print('Tuesday')
elif day == 5:
    print('Wednesday')
elif day == 6:
    print('Thrusday')
elif day == 7:
    print('Friday')
else:
    print('Invalid')